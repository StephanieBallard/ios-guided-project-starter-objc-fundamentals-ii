#import "SABTipController.h"
#import "SABTip.h" // Import header files in your .m files

// Class extension
@interface SABTipController () {
    // (Optional) Private instance variables
    
}

// Private Properties
@property (nonatomic, readwrite) NSMutableArray<SABTip *> *internalTips;
// Private Outlets

// Private Methods

@end

@implementation SABTipController

- (instancetype)init {
    self = [super init];
    if (self) {
        _internalTips = [[NSMutableArray alloc] init];
    }
    return self;
}

// Computed property (no autogenerated _instanceVariable)
- (NSArray<SABTip *> *)tips {
    // It is fast to return a copy in Objc/Swift
    return [_internalTips copy]; // copy: NSMutableArray -> NSArray (Copy on Write = COW)
}

- (void)addTip:(SABTip *)tip {
    [self.internalTips addObject:tip];
    
    // Future: sync to firebase or CoreData
}

@end
